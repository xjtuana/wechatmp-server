package org.ana.wcmp.model.vo.infoinquiry;

public class FluxCostIPInfo {
	
	private String Name;
	private String IP;
	private String NetID;
	private String Month;
	private String OutFlux;
	private String InFlux;
	private String TotalFlux;
	private String Cost;
	public String getIP() {
		return IP;
	}
	public void setIP(String iP) {
		IP = iP;
	}
	public String getNetID() {
		return NetID;
	}
	public void setNetID(String netID) {
		NetID = netID;
	}
	public String getMonth() {
		return Month;
	}
	public void setMonth(String month) {
		Month = month;
	}
	public String getOutFlux() {
		return OutFlux;
	}
	public void setOutFlux(String outFlux) {
		OutFlux = outFlux;
	}
	public String getInFlux() {
		return InFlux;
	}
	public void setInFlux(String inFlux) {
		InFlux = inFlux;
	}
	public String getTotalFlux() {
		return TotalFlux;
	}
	public void setTotalFlux(String totalFlux) {
		TotalFlux = totalFlux;
	}
	public String getCost() {
		return Cost;
	}
	public void setCost(String cost) {
		Cost = cost;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}

}
